package au.edu.adelaide.kahn.pn;

/**
 * Listener for change events in a network.  The
 * listener is notified of changes in the network
 * structure.
 *
 * @author Darren Webb
 * @version $Id: NetworkChangeListener.java,v 1.2 2005-08-02 11:54:55 cvsproject Exp $
 */
public interface NetworkChangeListener
{
	/**
	 * Notification of change in the network.
	 */
	public void processAdded(NetworkChangeEvent e);
	/**
	 * Notification of change in the network.
	 */
	public void processRemoved(NetworkChangeEvent e);
	/**
	 * Notification of change in the network.
	 */
	public void channelAdded(NetworkChangeEvent e);
	/**
	 * Notification of change in the network.
	 */
	public void channelRemoved(NetworkChangeEvent e);
}
